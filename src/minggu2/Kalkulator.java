package minggu2;

public class Kalkulator {
  int operan1, operan2;
  float hasil;

  public void tambah() {
    hasil = operan1 + operan2;
  }

  public void kurang() {
    hasil = operan1 - operan2;
  }

  public void kali() {
    hasil = operan1 * operan2;
  }

  public void bagi() {
    hasil = operan1 / operan2;
  }
}